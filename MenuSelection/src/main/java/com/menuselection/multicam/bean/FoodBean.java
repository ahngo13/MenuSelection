package com.menuselection.multicam.bean;

public class FoodBean {

	private int FOOD_NUM;
	private String FOOD_NAME;
	private String FOOD_KIND;
	private String FOOD_STATE;
	
	public int getFOOD_NUM() {
		return FOOD_NUM;
	}
	public void setFOOD_NUM(int fOOD_NUM) {
		FOOD_NUM = fOOD_NUM;
	}
	public String getFOOD_NAME() {
		return FOOD_NAME;
	}
	public void setFOOD_NAME(String fOOD_NAME) {
		FOOD_NAME = fOOD_NAME;
	}
	public String getFOOD_KIND() {
		return FOOD_KIND;
	}
	public void setFOOD_KIND(String fOOD_KIND) {
		FOOD_KIND = fOOD_KIND;
	}
	public String getFOOD_STATE() {
		return FOOD_STATE;
	}
	public void setFOOD_STATE(String fOOD_STATE) {
		FOOD_STATE = fOOD_STATE;
	}
	
	
	
}
